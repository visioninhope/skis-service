<template>
  <div class="container-fluid px-0" data-cy="nav">
    <div class="row skills-nav no-gutters">
      <div class="col-md-auto border rounded pt-3 pr-0 bg-light" data-cy="nav-col">
        <div class="mb-3 ml-3 text-secondary">
          <b-row no-gutters>
            <b-col v-if="smallScreenMode || !collapsed">
              <span class="h6 text-uppercase mr-2 nav-title" v-if="!collapsed || smallScreenMode">Navigation</span>
            </b-col>
            <b-col :class="{'text-right': !collapsed || smallScreenMode }">
              <div v-if="!smallScreenMode" :class="{ 'pr-2 pl-3' : !collapsed }">
                <b-button v-if="!smallScreenMode" size="sm" variant="outline-secondary" @click="flipCollapsed"
                          class="py-0 text-primary" style="border-color: #d8d8d9;" data-cy="navCollapseOrExpand"
                          v-b-tooltip.hover
                          :aria-label="collapsed ? 'Expand Navigation' : 'Collapse Navigation'"
                          :title="collapsed ? 'Expand Navigation' : 'Collapse Navigation'">
                  <i v-if="!collapsed" class="fas fa-compress-alt"/><i v-else class="fas fa-expand-alt"/>
                </b-button>
              </div>
              <div v-if="smallScreenMode" class="pr-2">
                <b-button v-b-toggle.menu-collapse-control variant="outline-secondary" size="sm" class="mb-1"
                          data-cy="navSmallScreenExpandMenu">
                  <i class="fas fa-bars" aria-hidden="true"/><span class="sr-only">navigation menu toggle</span>
                </b-button>
              </div>
            </b-col>
          </b-row>
        </div>

        <!-- bootstrap didn't handle vertical menus well so rolling out our own-->
        <b-collapse id="menu-collapse-control" :visible="!smallScreenMode">
          <ul class="p-0" style="list-style: none;">
            <router-link v-for="(navItem) of navItems"
                         :key="navItem.name"
                         :to="{ name: navItem.page }"
                          v-slot="{ href, navigate, isActive, isExactActive }"
                          custom>
              <li class="mb-1 p-2 text-primary"
                  :data-cy="`nav-${navItem.name}`"
                  v-b-tooltip="{ title: navItem.msg ? navItem.msg : navItem.name, placement: 'right', variant: 'primary', disabled: !collapsed && !navItem.isDisabled }"
                  :class="[isExactActive && 'bg-primary']">
                  <a :href="href"
                     @click="(e) => { conditionalCollapse(); navigate(e); }"
                     :class="[isExactActive && 'text-light', !isExactActive && 'select-cursor', navItem.isDisabled && 'disabled', isActive && 'router-link-active', isExactActive && 'router-link-exact-active']"
                     :aria-current="isExactActive ? 'page' : null">
                    <div class="text-truncate ml-3" :class="{'mr-4': !collapsed}" :aria-label="`Navigate to ${navItem.name} page`">
                      <i :class="navItem.iconClass" class="fas"
                         style="min-width: 1.7rem;" aria-hidden="true"/> <span v-if="!collapsed || smallScreenMode">{{ navItem.name }}</span>
                      <i v-if="navItem.isDisabled" class="fas fa-exclamation-circle text-warning ml-1" />
                    </div>
                  </a>
              </li>
            </router-link>
          </ul>
        </b-collapse>
      </div>
      <div class="col-md skills-menu-content" ref="content">
        <div class="container-fluid pb-4">
          <router-view id="mainContent2" tabindex="-1" aria-label="Main content area, click tab to navigate"></router-view>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
  export default {
    name: 'Navigation',
    props: ['navItems'],
    data() {
      return {
        collapsed: false,
        menuSelections: [],
        windowWidth: 0,
      };
    },
    created() {
      window.addEventListener('resize', this.handleResize);
      this.handleResize();
    },
    mounted() {
      this.collapsed = this.getCollapsedFromLocalStorage();
    },
    unmounted() {
      window.removeEventListener('resize', this.handleResize);
    },
    methods: {
      getCollapsedFromLocalStorage() {
        const storageCollapsed = localStorage.skillsNavCollapsed;
        this.collapsed = storageCollapsed === 'true' ? Boolean(storageCollapsed) : false;
        return this.collapsed;
      },
      flipCollapsed() {
        this.collapsed = !this.collapsed;
        localStorage.skillsNavCollapsed = this.collapsed;
      },
      handleResize() {
        this.windowWidth = window.innerWidth;
      },
      conditionalCollapse() {
        if (this.smallScreenMode) {
          // eslint-disable-next-line no-use-before-define
          this.$root.$emit('bv::toggle::collapse', 'menu-collapse-control');
        }
      },
    },
    computed: {
      smallScreenMode() {
        // 768 matches md in bootstrap
        return this.windowWidth < 768;
      },
    },
  };
</script>

<style scoped>
  .select-cursor {
    cursor: pointer;
  }

  @media (min-width: 768px) {
    .skills-nav {
      min-height: calc(100vh - 10rem);
    }
  }

  .nav-title {
    color: #3f5971;
  }

  .skills-menu-content {
    /* this little hack is required to prevent apexcharts from wrapping onto a new line;
    the gist is that they calculate width dynamically and do not work properly with the width of 0*/
    min-width: 1rem;
  }

  .skills-menu-content:focus {
    outline: none;
  }

</style>
